import { View } from "react-native";

const pattern = [
  "111110101011001011111",
  "100010010100101010001",
  "101110111010111010111",
  "101110001110001010111",
  "101110101011101010111",
  "100010001000101010001",
  "111110101010101011111",
  "000000010001000000000",
  "111011111010111101110",
  "001100001001001100100",
  "111011101110111111101",
  "100001001000100010001",
  "101111111011111011101",
  "001000100010001010001",
  "111010111110111011101",
  "000000001001000000000",
  "111110111010111110111",
  "100010001000100010001",
  "101110111011111010111",
  "100010100010001010001",
  "111110111111111011111",
];

export function FakeQrCode() {
  return (
    <View className="self-center rounded-[24px] bg-white p-4">
      {pattern.map((row, rowIndex) => (
        <View key={`qr-row-${rowIndex}`} className="flex-row">
          {row.split("").map((cell, cellIndex) => (
            <View
              key={`qr-cell-${rowIndex}-${cellIndex}`}
              style={{
                width: 10,
                height: 10,
                backgroundColor: cell === "1" ? "#000000" : "#ffffff",
              }}
            />
          ))}
        </View>
      ))}
    </View>
  );
}
